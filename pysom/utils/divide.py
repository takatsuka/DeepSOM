def divide(arg1: float, arg2: float) -> float:
    """
    Performs a divide Calculator operation on two floats and returns a float.

    Args:
        arg1 (float): the first float operand
        arg2 (float): the second float operand

    Returns:
        float: the division of arg1 by arg2
    """
    return float(arg1 / arg2)
