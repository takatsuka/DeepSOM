def add(arg1: float, arg2: float) -> float:
    """
    Performs an add Calculator operation on two floats and returns a float.

    Args:
        arg1 (float): the first float operand
        arg2 (float): the second float operand

    Returns:
        float: the addition of arg1 and arg2
    """
    return float(arg1 + arg2)
